    @Test
    @Transactional
    void putNewProfile() throws Exception {
        // Initialize the database
        profileRepository.saveAndFlush(profile);

        int databaseSizeBeforeUpdate = profileRepository.findAll().size();

        // Update the profile
        Profile updatedProfile = profileRepository.findById(profile.getId()).get();
        // Disconnect from session so that the updates on updatedProfile are not directly saved in db
        em.detach(updatedProfile);
        updatedProfile
            .displayName(UPDATED_DISPLAY_NAME)
            .balance(UPDATED_BALANCE)
            .latitude(UPDATED_LATITUDE)
            .longitude(UPDATED_LONGITUDE)
            .avatar(UPDATED_AVATAR)
            .phone(UPDATED_PHONE)
            .dob(UPDATED_DOB)
            .location(UPDATED_LOCATION)
            .countryCode(UPDATED_COUNTRY_CODE)
            .point(UPDATED_POINT);
        ProfileDTO profileDTO = profileMapper.toDto(updatedProfile);

        restProfileMockMvc
            .perform(
                put(ENTITY_API_URL_ID, profileDTO.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(profileDTO))
            )
            .andExpect(status().isOk());

        // Validate the Profile in the database
        List<Profile> profileList = profileRepository.findAll();
        assertThat(profileList).hasSize(databaseSizeBeforeUpdate);
        Profile testProfile = profileList.get(profileList.size() - 1);
        assertThat(testProfile.getDisplayName()).isEqualTo(UPDATED_DISPLAY_NAME);
        assertThat(testProfile.getBalance()).isEqualTo(UPDATED_BALANCE);
        assertThat(testProfile.getLatitude()).isEqualTo(UPDATED_LATITUDE);
        assertThat(testProfile.getLongitude()).isEqualTo(UPDATED_LONGITUDE);
        assertThat(testProfile.getAvatar()).isEqualTo(UPDATED_AVATAR);
        assertThat(testProfile.getPhone()).isEqualTo(UPDATED_PHONE);
        assertThat(testProfile.getDob()).isEqualTo(UPDATED_DOB);
        assertThat(testProfile.getLocation()).isEqualTo(UPDATED_LOCATION);
        assertThat(testProfile.getCountryCode()).isEqualTo(UPDATED_COUNTRY_CODE);
        assertThat(testProfile.getPoint()).isEqualTo(UPDATED_POINT);
    }

